#include<iostream>
using namespace std;
int main(){
    int ch;
    cin>>ch; 
    char x = (char)ch;    // Converts any assigned ASCII number to its respective character  
    cout<<x;  
}



// ASCII numbers : - 
//  1 to 31 = Emoji's and Reactions
//   32  = Space
//  33 to 47 = Symbols
//  48 to 57 = Numbers ( 0 --> 9 )
//  58 to 64 = Symbols
//  65 to 90 = Capital Letters ( A --> Z )
//  90 to 96 = Symbols
//  97 to 122 = small Letters ( a --> z )